// Definindo o primeiro valor como 500
int firstValue = 500;

// Definindo o segundo valor como 600
int secondValue = 600;

// Declarando uma variável para armazenar o valor maior entre os dois
int largerValue;

// Usando a função Math.Max para encontrar o valor maior entre firstValue e secondValue
largerValue = Math.Max(firstValue, secondValue);

// Imprimindo o valor maior na tela
Console.WriteLine(largerValue);
